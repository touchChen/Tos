; ========================================================================
;                  void disp_str(char * info);
; ========================================================================
disp_str:
	push	ebp
	mov	ebp, esp

	mov	esi, [ebp + 8]	; pszInfo
	mov	edi, [disp_pos]
	mov	ah, 0Fh
.1:
	lodsb
	test	al, al
	jz	.2
	cmp	al, 0Ah	; 是回车吗?
	jnz	.3
	push	eax
	mov	eax, edi
	mov	bl, 160
	div	bl
	and	eax, 0FFh
	inc	eax
	mov	bl, 160
	mul	bl
	mov	edi, eax
	pop	eax
	jmp	.1
.3:
	mov	[gs:edi], ax
	add	edi, 2
	jmp	.1

.2:
        cmp     edi, 4000
        ja      .4
        mov	[disp_pos], edi
        jmp     .5   
        
.4:     
        push    7
        call    clear_last_row
        add     esp, 4         
	
.5:
	pop	ebp
	ret



; ========================================================================
;                  void disp_color_str(char * info, int color);
; ========================================================================
disp_color_str:
